let a = 12

// With no Break he continues the switch

switch (a) {
    case 12:
        console.log("é 12");
    case 13:
        console.log("é 13")
    case 72:
        console.log("é 72")
    case 99:
        console.log("é 99")
        console.log("Will not continue the Switch case...")
        break;
    case 21:
        console.log("é 21")
    case 6:
        console.log("é 6")

}